import java.util.*;
public class Group {
  int groupPair;
  int groupNum;

  public Group(int groupPair, int groupNum) {
    this.groupPair = groupPair;
    this.groupNum = groupNum;
  }

  public int getGroupPair() {
    return this.groupPair;
  }

  public int getGroupNum() {
    return this.groupNum;
  }

  public void setGroupPair(int groupPair) {
    this.groupPair = groupPair;
  }

  public void setGroupNum(int VgroupNum) {
    this.groupNum = VgroupNum;
  }
}
